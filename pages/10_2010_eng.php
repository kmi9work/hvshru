                 Workplace: school № 1.
             </p>
    </div>
  <div class="exampleblock">
    <div class="title">Summary</div>
    <div class="exampleblock-content">
      <div class="paragraph">
        <p>The Scientific organization of pupils as a motivation to  research  work  in
profile classes. The authors N.G. Gabruk, V.V. Babukova.
In article analyze the problems of the organization a purposeful research
activity of senior pupils - members of a school scientific organization,
which functions the third year within the framework of cooperation "school - high school" on the base of algorithms of performancing the research
project «The Governor's program in operation: Qualitative bread is our
health ».
The choice of a vital topic of the  research  project,  use  of  simple  and
reliable methods of the quantitative analysis is a dominating motivation  in
the profound study of chemistry.
Authors allocate the basic stages of  the  research  project:  studying  the
demand of consumption of bread and bakery products by  some  layers  of  the
population with the help of sociological interrogation; adaptation a  method
of neutralization to techniques of quantitative  definition  of  acidity  of
bread and bakery products; carrying out a comparative  analysis  on  quality
of researched samples of bread. The article analyzes  the  consumer  demand,
lists the technique and results  of  definition  of  acidity  of  bread  and
bakery products.</p>
