<div class="paragraph"><div class="title">Authors:</div>
<p>
<b>Rumyantsev B.V.</b>. Adress: Moscow. Workplace: Moscow State Pedagogical University.
</p>
<p>
<b>Privalov A.A.</b>. Adress: Moscow. Workplace: Moscow State Pedagogical University.
</p>
</div>
<div class="exampleblock">
<div class="title">Summary</div>
<div class="exampleblock-content">
<div class="paragraph"><p>
The article deals with the mathematical rules of approximate calculations needed for the correct solution of chemical problems. Not only the rules of calculations are formulated, but the rules of choosing the calculation method as well, this being essential for solving chemical problems. As regard to mathematical aspect, the rules of rounding off, addition, subtraction and multiplication of approximate numbers are formulated as well as the rule of interpolation. As regard to chemical aspect, the approximate and exact values are considered. The implementation of the following tasks is examined: rounding off numerical values of physical parameters given in the task; planning mathematical calculations, finding missing values by means of interpolation as well as the selection and conversion of units of physical values. The authors emphasize the idea that approximate calculations should become an important part of mathematical education.
</p></div>
