  <div class="paragraph">
    <div class="title">Authors:</div>
             <p>
               <b> A. V.</b>.
                 Adress: Moscow.
                 Workplace: The 1st Moscow State Medical University named after I.M. Sechenov.
             </p>
             <p>
               <b> V. A.</b>.
                 Adress: Moscow.
                 Workplace: The 1st Moscow State Medical University named after I.M. Sechenov.
             </p>
    </div>
  <div class="exampleblock">
    <div class="title">Summary</div>
    <div class="exampleblock-content">
      <div class="paragraph">
        <p>The information regarding acid-base amphotericity of chemical compounds  was
summarized. This information  is  being  appeared  in  various  sections  of
chemistry course at secondary  school.  The  concept  of  amphotericity  was
proposed by G. Bredig in 1900. The different types of ampholytes  have  been
considered. There are one- and two-centered ampholytes, amphoteric  liquids,
metal hydroxides, acidic and basic salts, salts of weak acid and weak  base,
poly- and heterofunctional organic compounds, and polyampholytes. The  state
of ampholyte in solution has been  characterized  by  equilibrium  constants
and pH values of isoelectric point. The conditions of transformation of two-
centered ampholyte to zwitterion are given. The material will help  teachers
explain the essence of amphotericity, and in some cases, apply  quantitative
data on the equilibrium reactions of amphoteric compounds.</p>
      </div>
    </div>  
  </div>
  <div class="paragraph">
  <div class="title"> Keywords: </div>
    <p> Amphoteric  matters,   acid-base  amphotericity,    amphoteric   electrolytes, amphoteric liquids,  polyampholytes. </p>
